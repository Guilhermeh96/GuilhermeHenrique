$(document).ready(function() {

    $('.main').click(function(){
    $('body').removeClass();
    });

    $('.palette1').click(function(){
    $('body').removeClass();
    $('body').addClass('uiColorWhite');
    });

    $('.palette2').click(function(){
    $('body').removeClass();
    $('body').addClass('uiColorTurquoise');
    });

    $('.palette3').click(function(){
    $('body').removeClass();
    $('body').addClass('uiColorBlue');
    });

});
